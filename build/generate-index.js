#!/usr/bin/env node
/* SIM2FIELD landing-page generator.
   Produces index.html at the repo root: the book title, the 17 modules in reading
   order linking to their student editions (with a secondary link to each author
   edition), and links to the figure index, figure inventory, and the interactive
   instrument suite. Deterministic and self-contained: frozen Rev 1.2 CSS tokens, all
   relative links (works on GitHub Pages and localhost), no external dependencies.
   Module titles are read from each MODULE-NN-*.md heading (the module stays the single
   source of truth). Run: node generate-index.js  (writes ../index.html)
*/
"use strict";
const fs = require("fs");
const path = require("path");
const BUILD = __dirname, ROOT = path.resolve(BUILD, "..");
const esc = s => String(s).replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");

// modules in reading order (numeric); title from each module's H1.
const mods = fs.readdirSync(ROOT).filter(f => /^MODULE-\d\d-.*\.md$/.test(f)).sort();
const items = mods.map(f => {
  const NN = f.match(/^MODULE-(\d\d)/)[1];
  const h1 = fs.readFileSync(path.join(ROOT, f), "utf8").split("\n")[0].replace(/^#\s+/, "").trim();
  const m = h1.match(/^Module\s+(\d+):\s*(.+)$/);
  const n = m ? m[1] : String(Number(NN));
  const title = m ? m[2] : h1;
  return `<li><span class="mn">M${n}</span>`
    + `<a class="main" href="module-${NN}.html">${esc(title)}</a>`
    + `<a class="alt" href="module-${NN}.author.html">author</a></li>`;
}).join("\n");

const HEAD = `<!DOCTYPE html><html lang="en"><head><meta charset="UTF-8"><meta name="viewport" content="width=device-width, initial-scale=1.0"><meta name="color-scheme" content="dark"><title>SIM2FIELD</title><style>
:root{--bg:#0e120d;--surface:#161c14;--surface-2:#0b0e0a;--surface-3:#12160f;--border:#2b3527;--border-strong:#3a4633;--text-primary:#eef2e6;--text-secondary:#c8cfbe;--text-muted:#adb4a2;--text-on-accent:#0e120d;--accent:#9bcf3b;--accent-strong:#6f9a2c;--info:#6fb0d9;--warn:#e0a03b;--warn-text:#eed9b4;--danger:#f0556a;--danger-text:#f697a4;--accent-2:#b98bd9}*{box-sizing:border-box}body{margin:0;background:var(--bg);color:var(--text-primary);font-family:'IBM Plex Mono',ui-monospace,monospace;padding:2rem 2.2rem 4rem;max-width:960px}
h1{font-family:'Space Grotesk',system-ui,sans-serif;font-size:1.6rem;margin:0 0 .3rem}
.sub{color:var(--text-muted);font-size:.85rem;line-height:1.5;margin:.2rem 0 1.8rem;max-width:78ch}
h2{font-family:'Space Grotesk',system-ui,sans-serif;font-size:1.02rem;border-bottom:1px solid var(--border);padding-bottom:.4rem;margin:2rem 0 1rem}
ol.mods{list-style:none;padding:0;margin:0}
ol.mods li{display:flex;align-items:baseline;gap:.7rem;padding:.45rem .7rem;border:1px solid var(--border);border-radius:7px;margin-bottom:.4rem;background:var(--surface)}
.mn{color:var(--accent);font-weight:700;font-size:.66rem;min-width:2.4rem}
a.main{color:var(--text-primary);text-decoration:none;font-size:.82rem;flex:1}a.main:hover{color:var(--accent)}
a.alt{color:var(--text-muted);font-size:.64rem;text-decoration:none;border:1px solid var(--border-strong);border-radius:5px;padding:.05rem .4rem}a.alt:hover{color:var(--text-secondary)}
ul.res{list-style:none;padding:0;margin:0}
ul.res li{padding:.4rem .7rem;border:1px solid var(--border);border-radius:7px;margin-bottom:.4rem;background:var(--surface);font-size:.8rem}
ul.res a{color:var(--info);text-decoration:none}ul.res a:hover{text-decoration:underline}
ul.res .d{color:var(--text-muted);font-size:.72rem}
footer{margin-top:2.5rem;color:var(--text-muted);font-size:.72rem;border-top:1px solid var(--border);padding-top:1rem;line-height:1.5;max-width:80ch}
</style></head><body>
<header><h1>SIM2FIELD</h1>
<p class="sub">Autonomous Watermelon Harvesting Robotics. A 17-module digital textbook; each module renders to a student edition and an author edition.</p></header>
<h2>Modules</h2>
<ol class="mods">
`;

const RES = `
</ol>
<h2>Figures and resources</h2>
<ul class="res">
<li><a href="figure-index.html">Figure index</a> <span class="d">- gallery of the 108 built figures and the interactive 3D demos.</span></li>
<li><a href="figure-inventory.html">Figure inventory</a> <span class="d">- every figure slot classified (built vs capture-preferred).</span></li>
<li><a href="instruments.html">Interactive instruments</a> <span class="d">- the worked-example instrument suite.</span></li>
</ul>
<footer>Generated by build/generate-index.js from the module set. All links are relative, so this works on GitHub Pages and localhost with no external dependencies.</footer>
</body></html>
`;

fs.writeFileSync(path.join(ROOT, "index.html"), HEAD + items + RES);
console.log(`wrote index.html (${mods.length} modules)`);
