"use strict";
/* publish-student.js - build the PUBLIC student site into dist-public/ and push it to the
   student book repo. Emits ONLY: the 17 student module editions, index.html,
   figure-index.html, figure-inventory.html, the demo/sim assets those pages reference, and
   the vendor/ tree (fonts, KaTeX, three.js). Figures are inline SVG in the pages, so no
   F-*.svg files are referenced or copied.

   Absolutely excluded: author editions, solutions, homework check scripts, module markdown,
   SESSION-LOG, CAPTURE-LIST, the guards and build scripts, the curriculum register, and the
   instruments aggregator (not in the published set).

   Out-of-set references are neutralized at publish time (not in the private source):
     - index.html      : author-edition links, the instruments link, the build/ footer note
     - each module     : the curriculum register link (unwrapped to text), and the
                          "source: MODULE-NN.md" provenance in the top bar and footer

   A leak check then fails the build (before any push) if any emitted page carries an
   author/solution content marker, or references an excluded/unpublished file, or has a local
   link that does not resolve inside dist-public. On success it force-pushes dist-public/ to
   the student book repo main branch.

   Run: node publish-student.js            (from build/) */
const fs = require("fs"), path = require("path"), cp = require("child_process");

const ROOT = path.dirname(__dirname);
const DIST = path.join(ROOT, "dist-public");
const BOOK_REMOTE = "https://github.com/alibulentkoc/sim2field-book.git";
const PUSH = !process.argv.includes("--no-push");   // --no-push: build + leak-check only

// ---------- the export set ----------
const MODULES = Array.from({ length: 17 }, (_, i) => `module-${String(i + 1).padStart(2, "0")}.html`);
const DEMOS = [
  "CEC01-signal-to-action-spine-worked-example.html",
  "CEC05-sim-to-real-pipeline-worked-example.html",
  "CEC06-fidelity-ladder-worked-example.html",
  "M4-detection-operating-point-worked-example.html",
  "M5-placement-error-budget-worked-example.html",
  "M6-edge-latency-boundary-worked-example.html",
  "M8-kinematics-singularity-explorer.html",
  "M9-grip-force-window-worked-example.html",
  "harvesting-robot-sim.html",
  "watermelon-harvester-amiga.html",
];
const THUMBS = ["harvesting-robot-sim.thumb.png", "watermelon-harvester-amiga.thumb.png"];
const COPY_ASIS = ["figure-index.html", "figure-inventory.html", ...DEMOS, ...THUMBS];

// ---------- 1. reset dist-public (preserve .git so the book repo keeps its history) ----------
if (fs.existsSync(DIST)) {
  for (const e of fs.readdirSync(DIST)) { if (e === ".git") continue; fs.rmSync(path.join(DIST, e), { recursive: true, force: true }); }
} else {
  fs.mkdirSync(DIST, { recursive: true });
}

// ---------- 2. copy vendor tree + as-is assets ----------
fs.cpSync(path.join(ROOT, "vendor"), path.join(DIST, "vendor"), { recursive: true });
for (const f of COPY_ASIS) fs.copyFileSync(path.join(ROOT, f), path.join(DIST, f));

// ---------- 3. transform + emit index.html ----------
let idx = fs.readFileSync(path.join(ROOT, "index.html"), "utf8");
idx = idx.replace(/<a class="alt" href="module-\d\d\.author\.html">author<\/a>/g, "");
idx = idx.replace(/<li><a href="instruments\.html">[\s\S]*?<\/li>\n?/, "");
idx = idx.replace("Generated by build/generate-index.js from the module set.", "Generated from the module set.");
fs.writeFileSync(path.join(DIST, "index.html"), idx);

// ---------- 4. transform + emit the 17 student modules ----------
for (const m of MODULES) {
  let h = fs.readFileSync(path.join(ROOT, m), "utf8");
  h = h.replace(/<span class="src">source: MODULE-\d\d\.md<\/span>/, "");
  h = h.replace(/Rendered from <b>MODULE-\d\d\.md<\/b> by Module Rendering Pipeline v1\.3\./, "Rendered by the Module Rendering Pipeline.");
  h = h.replace(/<a href="curriculum\/_core-concepts\.md">([^<]*)<\/a>/g, "$1");
  fs.writeFileSync(path.join(DIST, m), h);
}

// ---------- 5. leak + link checks (fail before any push) ----------
const emitted = [];
(function walk(d) { for (const e of fs.readdirSync(d, { withFileTypes: true })) { const p = path.join(d, e.name); if (e.name === ".git") continue; e.isDirectory() ? walk(p) : emitted.push(p); } })(DIST);
const rel = p => path.relative(DIST, p).replace(/\\/g, "/");

// author/solution CONTENT that must never appear publicly (precise HTML, not prose): raw scan.
const CONTENT_MARKERS = [/<body class="ed-author"/, /<details class="answers/, /Author \/ production edition/, /Homework solutions \(instructor\)/];
// excluded/unpublished targets, checked against href/src VALUES only (module prose may legitimately
// mention paths like "build/deploy" or <code>curriculum/...md</code>, which are content, not links).
const EXCLUDED_REF = [/\.author\.html/, /^curriculum\//, /\.md([#?]|$)/, /^solutions\//, /SESSION-LOG/, /CAPTURE-LIST/, /^instruments\.html/, /^build\//, /-src\.html/, /^checks\//];
const TEXT = /\.(html|css|js|svg)$/;

const problems = [];
for (const p of emitted) {
  if (!TEXT.test(p)) continue;                                   // skip binary (thumbnails)
  const txt = fs.readFileSync(p, "utf8");
  for (const re of CONTENT_MARKERS) if (re.test(txt)) problems.push(`${rel(p)}: author/solution content marker ${re}`);
  if (p.endsWith(".html") && !rel(p).startsWith("vendor/")) {
    for (const m of txt.matchAll(/(?:href|src)="([^"]+)"/g)) {
      const r = m[1];
      if (r.startsWith("#") || /^(https?:|data:|mailto:)/.test(r)) continue;
      if (EXCLUDED_REF.some(re => re.test(r))) { problems.push(`${rel(p)}: link to excluded/unpublished file -> ${r}`); continue; }
      const clean = r.split("#")[0].split("?")[0];             // every remaining local link must resolve in the set
      if (clean && !fs.existsSync(path.resolve(path.dirname(p), clean))) problems.push(`${rel(p)}: unresolved local link -> ${r}`);
    }
  }
}
if (!fs.existsSync(path.join(DIST, "vendor", "three", "0.160.0"))) problems.push("vendor/three/0.160.0 missing (3D sim dependency)");

if (problems.length) {
  console.error(`LEAK/LINK CHECK FAILED (${problems.length}) - nothing published:`);
  for (const x of problems.slice(0, 60)) console.error("  - " + x);
  process.exit(1);
}
const htmlCount = emitted.filter(p => p.endsWith(".html")).length;
console.log(`leak+link check PASSED: ${emitted.length} files emitted (${htmlCount} html), all clean and self-contained`);

// ---------- 6. publish to the student book repo ----------
if (!PUSH) { console.log("--no-push: built and checked only; not publishing"); process.exit(0); }
const git = (...a) => cp.execFileSync("git", ["-C", DIST, ...a], { stdio: ["ignore", "inherit", "inherit"] });
if (!fs.existsSync(path.join(DIST, ".git"))) {
  git("init", "-q");
  git("checkout", "-q", "-B", "main");
  git("remote", "add", "origin", BOOK_REMOTE);
}
git("add", "-A");
const MSG = "Publish SIM2FIELD student site (17 modules, index, figures, demos)\n\n" +
  "Generated by build/publish-student.js from the private SIM2FIELD repo. Student\n" +
  "editions only; author editions, solutions, checks, and internal docs excluded and\n" +
  "leak-checked. All links resolve within the exported set.\n\n" +
  "Co-Authored-By: Claude Opus 4.8 (1M context) <noreply@anthropic.com>";
try {
  cp.execFileSync("git", ["-C", DIST, "-c", "user.email=alibkoc@gmail.com", "-c", "user.name=SIM2FIELD Publisher", "commit", "-q", "-m", MSG], { stdio: ["ignore", "inherit", "inherit"] });
} catch (e) {
  console.log("(no changes since last publish)");
}
git("push", "-f", "origin", "main");
console.log(`published dist-public/ to ${BOOK_REMOTE} (main)`);
